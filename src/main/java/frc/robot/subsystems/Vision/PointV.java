package frc.robot.subsystems.Vision;

public class PointV {
    public double x;
    public double y;
    public double z;

    public PointV(double x, double y){
        this.x = x;
        this.y = y;
    }

    public PointV (double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
